#include "libft.h"

int	ft_isalnum(int ch)
{
	if (!((ch > 64 && ch < 91) || (ch > 96 && ch < 123)
			|| (ch >= '0' && ch <= '9')))
	{
		return (0);
	}
	return (1);
}
