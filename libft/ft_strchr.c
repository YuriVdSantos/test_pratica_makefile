#include "libft.h"

char	*ft_strchr(const char *s, int i)
{
	i = (unsigned char)i;
	while (*s)
	{
		if (*s == i)
			return ((char *)s);
		s++;
	}
	if (i == '\0')
		return ((char *)s);
	return (0);
}
